    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        try {
            boolean idEncontrado = ca.verificarId(id);

            if (idEncontrado) {
                JOptionPane.showMessageDialog(null, "ID encontrado! Prosseguindo para o painel de pagamentos!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                new Vendas().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "ID não encontrado!", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // Log ou manipulação adequada da exceção
            JOptionPane.showMessageDialog(null, "Erro ao verificar ID!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


