    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        if (ControllerControleVendas.HistoricoId(id)) {
            try {
                ccv.deletarDado(id);
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
            JOptionPane.showMessageDialog(rootPane, "Venda removida com sucesso!");
            new TelaControle().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Venda com o ID citado, não existe!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed


